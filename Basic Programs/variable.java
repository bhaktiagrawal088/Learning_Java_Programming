public class variable {
    public static void main(String args[]) {
        int x = 10; // declaring an integer type variable named 'x' and assigning
        System.out.println(x); // 10
        float y = 34.56f; // declaring a floating point number
        System.out.println(y); // 34.56
        boolean z = true; // declaring a Boolean data type variable with the name 'z
        // and assign it the value of false
        System.out.println(z); // True
        char c = 'A'; // character data type variable named 'c' and
        // assigned the ASCII value of A which is 65
        System.out.println(c); // A
        String s = "Hello"; // string data type variable named 's' and assigned
        // the string Hello
        System.out.println(s + " Bhakti Agrawal"); // Hello</s>

    }
}
